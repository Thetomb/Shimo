// 获取当前页面语言
function getCurrentLanguage() {
    const htmlLang = document.documentElement.lang;
    if (htmlLang === 'en') return 'en';
    if (htmlLang === 'de-DE') return 'de';
    return 'en'; // Default English
}

// 显示团队成员详情模态框
function showMemberDetails(member) {
    const language = getCurrentLanguage();
    let memberDetails;
    
    switch(language) {
        case 'en':
            memberDetails = memberDetailsEN;
            break;
        case 'de':
            memberDetails = memberDetailsDE;
            break;
        default:
            memberDetails = memberDetailsEN;
    }
    
    document.getElementById('memberDetails').innerHTML = memberDetails[member];
    document.getElementById('memberModal').style.display = 'block';
}

// 关闭模态框
function closeModal(modalId) {
    document.getElementById(modalId).style.display = 'none';
}

// 初始化事件
document.addEventListener('DOMContentLoaded', function() {
    // 团队成员详情按钮事件
    const viewDetailsButtons = document.querySelectorAll('.view-details-btn');
    viewDetailsButtons.forEach(button => {
        button.addEventListener('click', () => {
            const member = button.closest('.team-member').dataset.member;
            showMemberDetails(member);
        });
    });

    // 关闭模态框事件
    const closeButtons = document.querySelectorAll('.close');
    closeButtons.forEach(button => {
        button.addEventListener('click', () => {
            const modal = button.closest('.modal');
            if (modal) {
                modal.style.display = 'none';
            }
        });
    });

    // 点击模态框外部关闭模态框
    window.addEventListener('click', (event) => {
        if (event.target.classList.contains('modal')) {
            event.target.style.display = 'none';
        }
    });
});

// 图片查看器模态框
document.addEventListener('DOMContentLoaded', function() {
    const galleryButtons = document.querySelectorAll('.gallery-zoom-btn');
    galleryButtons.forEach(button => {
        button.addEventListener('click', () => {
            const imageSrc = button.dataset.image;
            document.getElementById('modalImage').src = imageSrc;
            document.getElementById('imageModal').style.display = 'block';
        });
    });

    // 发展历程时间轴交互
    const timelineItems = document.querySelectorAll('.timeline-item');
    timelineItems.forEach(item => {
        item.addEventListener('mouseenter', () => {
            item.classList.add('active');
        });
        item.addEventListener('mouseleave', () => {
            item.classList.remove('active');
        });
    });
});
